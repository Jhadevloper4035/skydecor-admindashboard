// Bulk delete QR codes
exports.deleteMultipleQRCodes = async (req, res) => {
  try {
    const { ids } = req.body;

    if (!ids || !Array.isArray(ids) || ids.length === 0) {
      return res.status(400).json({ error: "Please provide an array of IDs to delete." });
    }

    const result = await Qrcode.deleteMany({ id: { $in: ids } });

    res.status(200).json({
      status: "success",
      message: `${result.deletedCount} QR code(s) deleted successfully`,
      data: { deletedCount: result.deletedCount },
    });
  } catch (error) {
    console.error("Error deleting QR codes:", error);
    res.status(500).json({ error: "An error occurred while deleting QR codes." });
  }
};

// Search QR codes
exports.searchQRCodes = async (req, res) => {
  try {
    const { q, page = 1, limit = 50 } = req.query;
    const skip = (parseInt(page) - 1) * parseInt(limit);

    if (!q) {
      return res.status(400).json({ error: "Search query is required." });
    }

    const searchQuery = {
      $or: [
        { productCode: { $regex: q, $options: "i" } },
        { productName: { $regex: q, $options: "i" } },
        { category: { $regex: q, $options: "i" } },
        { subcategory: { $regex: q, $options: "i" } },
        { productType: { $regex: q, $options: "i" } }
      ]
    };

    const [qrcodes, total] = await Promise.all([
      Qrcode.find(searchQuery)
        .sort({ createdAt: -1 })
        .skip(skip)
        .limit(parseInt(limit))
        .lean(),
      Qrcode.countDocuments(searchQuery)
    ]);

    res.status(200).json({
      status: "success",
      message: "QR codes search results",
      currentPage: parseInt(page),
      totalPages: Math.ceil(total / parseInt(limit)),
      total,
      data: qrcodes,
    });
  } catch (error) {
    console.error("Error searching QR codes:", error);
    res.status(500).json({ error: "An error occurred while searching QR codes." });
  }
};

