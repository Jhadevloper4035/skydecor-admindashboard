const Qrcode = require("../model/qrcode.model.js");
const { sendExcelDownload } = require("../utils/excel.js");

const formatQRCodeForExport = (qrCode) => ({
  ID: qrCode.id ?? "",
  "Product Code": qrCode.productCode || "",
  "Product Name": qrCode.productName || "",
  Category: qrCode.category || "",
  "Sub Category": qrCode.subcategory || "",
  "Product Type": qrCode.productType || "",
  Status: qrCode.status || "",
  "Scan Count": qrCode.scanCount ?? 0,
  "Last Scanned At": qrCode.lastScannedAt ? new Date(qrCode.lastScannedAt).toISOString() : "",
  "Expiry Date": qrCode.expiryDate ? new Date(qrCode.expiryDate).toISOString() : "",
  "QR Image URL": qrCode.qrCodeImage || "",
  "QR Link": qrCode.linkInQrCode || "",
  "Product PDF URL": qrCode.productPdfPath || "",
  "Product Image URL": qrCode.productImageUrl || "",
  "Generated By": qrCode.generatedBy || "",
  "Created At": qrCode.createdAt ? new Date(qrCode.createdAt).toISOString() : "",
  "Updated At": qrCode.updatedAt ? new Date(qrCode.updatedAt).toISOString() : "",
});

// Get all QR codes with pagination
exports.getQRCodes = async (req, res) => {
  try {
    const page = parseInt(req.query.page, 10) || 1;
    const limit = parseInt(req.query.limit, 10) || 2000;
    const skip = (page - 1) * limit;

    const [qrcodes, total] = await Promise.all([
      Qrcode.find({})
        .sort({ createdAt: -1 })
        .skip(skip)
        .limit(limit)
        .lean(),
      Qrcode.countDocuments({})
    ]);

    const totalPages = Math.ceil(total / limit);

    res.status(200).json({
      status: "success",
      message: "QR codes fetched successfully",
      currentPage: page,
      totalPages,
      total,
      data: qrcodes,
    });
  } catch (error) {
    console.error("Error fetching QR codes:", error);
    res
      .status(500)
      .json({ error: "An error occurred while fetching QR codes." });
  }
};

exports.downloadQRCodes = async (req, res) => {
  try {
    const qrcodes = await Qrcode.find({})
      .sort({ createdAt: -1 })
      .select("-__v -scanHistory")
      .lean();

    sendExcelDownload(res, qrcodes.map(formatQRCodeForExport), "QR-Codes.xlsx");
  } catch (error) {
    console.error("Error downloading QR codes:", error);
    res.status(500).json({ error: "An error occurred while downloading QR codes." });
  }
};

