// Create new QR code
exports.createQRCode = async (req, res) => {
  try {
    const {
      id, productCode, productName, category, subcategory,
      productType, qrCodeImage, linkInQrCode, productPdfPath
    } = req.body;

    // Validation
    if (!id || !productCode || !productName || !category || 
        !subcategory || !productType || !qrCodeImage) {
      return res.status(400).json({ 
        error: "Required fields: id, productCode, productName, category, subcategory, productType, qrCodeImage" 
      });
    }

    // Check if ID already exists
    const existing = await Qrcode.findOne({ id });
    if (existing) {
      return res.status(409).json({ error: "QR code with this ID already exists." });
    }

    const qrcode = new Qrcode({
      id,
      productCode,
      productName,
      category,
      subcategory,
      productType,
      qrCodeImage,
      linkInQrCode: linkInQrCode || "",
      productPdfPath: productPdfPath || ""
    });

    const saved = await qrcode.save();

    res.status(201).json({
      status: "success",
      message: "QR code created successfully",
      data: saved,
    });
  } catch (error) {
    console.error("Error creating QR code:", error);
    res.status(500).json({ error: error.message || "An error occurred while creating QR code." });
  }
};

// Update QR code
exports.updateQRCode = async (req, res) => {
  try {
    const { id } = req.params;
    const {
      productCode, productName, category, subcategory,
      productType, qrCodeImage, linkInQrCode, productPdfPath
    } = req.body;

    const update = {};
    if (productCode      !== undefined) update.productCode      = productCode;
    if (productName      !== undefined) update.productName      = productName;
    if (category         !== undefined) update.category         = category;
    if (subcategory      !== undefined) update.subcategory      = subcategory;
    if (productType      !== undefined) update.productType      = productType;
    if (qrCodeImage     !== undefined) update.qrCodeImage     = qrCodeImage;
    if (linkInQrCode    !== undefined) update.linkInQrCode    = linkInQrCode;
    if (productPdfPath  !== undefined) update.productPdfPath  = productPdfPath;

    const updated = await Qrcode.findOneAndUpdate(
      { id: parseInt(id) },
      { $set: update },
      { new: true, runValidators: true }
    );

    if (!updated) {
      return res.status(404).json({ error: "QR code not found." });
    }

    res.status(200).json({
      status: "success",
      message: "QR code updated successfully",
      data: updated,
    });
  } catch (error) {
    console.error("Error updating QR code:", error);
    res.status(500).json({ error: error.message || "An error occurred while updating QR code." });
  }
};

